MODULE SCATT_PARAMS

   use type_constants, only: DP

   implicit none

   private 

   integer, parameter, public :: SCATT_PAR_KIRKLAND = 1, SCATT_PAR_LOBATO = 2, &
                                 SCATT_PAR_DOYLE_TURNER = 3

   integer, parameter :: NPARKIRK = 12
   integer, parameter :: NPARLOBA = 10
   integer, parameter :: NPARDT   = 8

   interface fact_electron
   module procedure fact_electron_s, fact_electron_v
   end interface

   public fact_electron, get_electron_params

contains

   real(DP) function fact_electron_s(par,q2,scatt_par_type) result(fe)
!
!  return the electron scattering factor for atomic
!  q2  = q*q where q =1/d = sin(theta)/lambda (in 1/A)
!
   real(DP), dimension(:), intent(in) :: par
   real(DP), intent(in)               :: q2
   integer, intent(in)                :: scatt_par_type
!
   select case(scatt_par_type)
     case (SCATT_PAR_KIRKLAND)
           fe = fact_electron_kirkland_s(par,q2)
     case (SCATT_PAR_LOBATO)
           fe = fact_electron_lobato_s(par,q2)
     case (SCATT_PAR_DOYLE_TURNER)
           fe = fact_electron_doyle_turner_s(par,q2)
   end select
!
   end function fact_electron_s

!----------------------------------------------------------------------------------------------------------------

   function fact_electron_v(par,q2,scatt_par_type) result(fe)
   real(DP), dimension(:), intent(in) :: par
   real(DP), dimension(:), intent(in) :: q2
   integer, intent(in)                :: scatt_par_type
   real(DP), dimension(size(q2))      :: fe
   integer                            :: i

   do i=1,size(q2)
      fe(i) = fact_electron_s(par,q2(i),scatt_par_type)
   enddo

   end function fact_electron_v

!----------------------------------------------------------------------------------------------------------------
   real(DP) function fact_electron_kirkland_s(par,q2) result(fe)
!
!  return the electron scattering factor for atomic
!  q2  = q*q where q =1/d = sin(theta)/lambda (in 1/A)
!
   real(DP), dimension(NPARKIRK), intent(in) :: par
   real(DP), intent(in)                      :: q2
   integer                                   :: i
   integer, parameter                        :: NL = NPARKIRK/2
   
   fe = 0.0
!
!  Lorenztians
   do i=1,NL,2
      fe = fe + par(i) / (q2 + par(i+1))
   enddo
!
!  Gaussians
   do i=NL+1,NPARKIRK,2
      fe = fe + par(i) * exp(-par(i+1)*q2)
   enddo

   end function fact_electron_kirkland_s

!----------------------------------------------------------------------------------------------------------------

   real(DP) function fact_electron_lobato_s(par,q2) result(fe)
!
!  return the electron scattering factor for atomic
!  q2  = q*q where q =1/d = sin(theta)/lambda (in 1/A)
!
   real(DP), dimension(NPARLOBA), intent(in) :: par
   real(DP), intent(in)                      :: q2
   integer, parameter                        :: NP = NPARLOBA/2
   real(DP), dimension(NP)                   :: q2_par
   integer                                   :: i

   q2_par(:) = par(6:NPARLOBA)*q2
   fe = 0
   do i=1,NP
      fe = fe + par(i)*(2 + q2_par(i)) / (1 + q2_par(i))**2
   enddo

   end function fact_electron_lobato_s 

!----------------------------------------------------------------------------------------------------------------

   real(DP) function fact_electron_doyle_turner_s(par,q2) result(fe)
!
!  return the electron scattering factor for atomic
!  q2  = q*q where q =1/d = sin(theta)/lambda (in 1/A)
!
   real(DP), dimension(NPARDT), intent(in) :: par
   real(DP), intent(in)                    :: q2
   integer                                 :: i
   integer, parameter                      :: NL = NPARKIRK/2
   
   fe = 0.0
!
   do i=1,NPARDT,2
      fe = fe + par(i) * exp(-par(i+1)*q2)
   enddo

   end function fact_electron_doyle_turner_s

!----------------------------------------------------------------------------------------------------------------

   subroutine get_electron_params(zval,charge,fact,scatt_par_type,ier)
   integer, intent(in)                 :: zval
   integer, intent(in)                 :: charge
   real(DP), dimension(:), intent(out) :: fact
   integer, intent(in)                 :: scatt_par_type
   integer, intent(out)                :: ier
!
   ier = 1
   select case(scatt_par_type)
     case (SCATT_PAR_KIRKLAND)
         call get_kirkland_params(zval,fact,ier)
     case (SCATT_PAR_LOBATO)
         call get_lobato_params(zval,fact,ier)
     case (SCATT_PAR_DOYLE_TURNER)
         call get_doyle_turner_params(zval,charge,fact,ier)
   end select
!
   end subroutine get_electron_params

