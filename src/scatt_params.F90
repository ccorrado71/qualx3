   subroutine make_doyle_turner_params(filename1,filename2,err)
   use fileutil
   use errormod
   use strutil
   use iso_fortran_env, only: stdout => output_unit
   character(len=*), intent(in)                :: filename1,filename2
   type(error_type), intent(out)               :: err
   type(file_handle)                           :: filep1,filep2
   integer                                     :: ier, nline, nword, nword2
   character(len=:), allocatable               :: line, line2
   character(len=:), allocatable, dimension(:) :: wordv1,wordv2
   integer                                     :: i
   logical :: is_ion
!
   call filep1%fopen(filename1)
   call filep2%fopen(filename2)
   if (filep1%fail() .or. filep2%fail()) then
       call err%set(filep1%err_msg())
       return
   endif

   write(stdout,'(3x,a)')'subroutine get_doyle_turner_params(zval,charge,fact,ier)'
   write(stdout,'(a)')'!'
   write(stdout,'(a)')'!  P. A. Doyle and P. S. Turner, Acta Cryst. (1968). A24, 390'
   write(stdout,'(a)')'!  Relativistic Hartree-Foek X-ray and Electron Scattering Factors'
   write(stdout,'(a)')'!'
   write(stdout,'(3x,a)')'use type_constants, only: DP'
   write(stdout,'(3x,a)')'integer, intent(in)                        :: zval'
   write(stdout,'(3x,a)')'integer, intent(in)                        :: charge'
   write(stdout,'(3x,a)')'real(DP), dimension(NPARDT), intent(out)   :: fact'
   write(stdout,'(3x,a)')'integer, intent(out)                       :: ier'
   write(stdout,'(a/3x,a/a)')   '!','ier = 0','!'
   write(stdout,'(a)')   '!  Order of parameters:'
   write(stdout,'(a)')   '!                        a1       b1       a2       b2       a3       b3       a4       b4'
   write(stdout,'(3x,a)')'select case(zval)'

   do while(get_line(filep1%handle(),line,trimmed=.true.,filter=.true.))
      if (len_trim(line) == 0) cycle
      if (is_comment_line(line)) cycle

      call get_words1(line,wordv1,nword)
      write(stdout,'(3x,a,a,a,a)')'case(',trim(adjustl(wordv1(1))),')   !',wordv1(2)

      is_ion = .false.
      do while(get_line(filep2%handle(),line2,trimmed=.true.,filter=.true.))
         call get_words1(line2,wordv2,nword2)
         if (wordv1(1) == wordv2(1)) then
             is_ion = .true.
         endif
      enddo
      call filep2%rew()

      if (is_ion) then
          write(stdout,'(6x,a)') 'if (charge == 0) then'
          write(stdout,'(10x,a,7(a8,","))',advance='no')    &
                       'fact(:) = [',[(adjustr(wordv1(i)),i=3,nword-1)]
          write(stdout,'(a8,"]")')adjustr(wordv1(nword))
          do while(get_line(filep2%handle(),line2,trimmed=.true.,filter=.true.))
             call get_words1(line2,wordv2,nword2)
             if (wordv1(1) == wordv2(1)) then
                 write(stdout,'(6x,a,a,a)') 'elseif (charge == ',trim(wordv2(3)),') then'
                 write(stdout,'(10x,a,7(a8,","))',advance='no')    &
                              'fact(:) = [',[(adjustr(wordv2(i)),i=4,nword2-1)]
                 write(stdout,'(a8,"]")')adjustr(wordv2(nword2))
             endif
          enddo
